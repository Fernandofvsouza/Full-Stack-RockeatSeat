//Operador condicional ternário
let age = 16;

//Sintaxe: condição ? valorSeVerdadeiro : valorSeFalso
let canDrive = age >= 18 ? "Pode dirigir" : "Não pode dirigir";
console.log(canDrive); //Saída: "Não pode dirigir"
//Chama ternario porque temos tres partes: a condição, o valor se verdadeiro e o valor se falso