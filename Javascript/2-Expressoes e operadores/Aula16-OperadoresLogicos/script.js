//Operadores logicos
let email = true;
let senha = true;

//Operador E (&&)
//Retorna true apenas se ambos os valores forem true
console.log(email && senha); //true, ambos os valores sao true

//Operador OU (||)
//Retorna true se um dos valores forem true
//Retorna false apenas se ambos os valores forem false
console.log(email || senha); //true, pelo menos um dos valores Ã© true

//Operador Not (!);
//Inverte o valor de um booleano
console.log(!email);//False porque inverteu o valor de true para false
console.log(!senha);//False porque inverteu o valor de true para false


