//Operadores de incremento e decremento
let number = 10;

//Incremento
number++; //equivalente a number = number + 1
console.log(number); //11

//Decremento
number--; //equivalente a number = number - 1
console.log(number); //10

//Incremento de 5 em 5
number += 5; //equivalente a number = number + 5
console.log(number); //15

//Decremento de 3 em 3
number -= 3; //equivalente a number = number - 3
console.log(number); //12

//Incremento de 2 em 2
number *= 2; //equivalente a number = number * 2
console.log(number); //24

//Decremento dividindo por 4
number /= 4; //equivalente a number = number / 4
console.log(number); //6