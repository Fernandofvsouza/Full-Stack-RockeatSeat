/*
--Escopo é a região do codigo onde determinada variavel ou função pode ser acessada.

--O escopo define o contexto no qual uma variavel pode ser referenciada ou moodificada
exemplo: tente pensar no contexto como comodos de uma casa, e para utilizar o fogão você precisa estar no mesmo comodo que ele.

Tipos de escopo:
*Escopo global: variaveis declaradas fora de qualquer função ou bloco de codigo (var)
*Escopo de bloco: acessiveis apenas dentro do bloco de codigo onde foram declaradas (let, const)
*Escopo local ou de função: variaveis declaradas dentro de uma função (let, const)

Exemplo: a cidade de são paulo (bloco/local) pertence ao contexto brasil (global)
 */